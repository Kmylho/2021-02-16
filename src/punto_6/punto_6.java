package punto_6;

public class punto_6 {
    public static void main(String[] args) {
        System.out.println("Numero pares");
        for (int i=1;i<51;i++){
            int j =i*2;
            System.out.println(j);
        }
        System.out.println("Numeros impares");
        for (int i=1;i<51;i++){
            int j =(i*2)-1;
            System.out.println(j);
        }
    }
}
