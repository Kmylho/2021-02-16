package punto_5;

public class punto_5 {
    public static void main(String[] args) {
        System.out.println("Numeros impares 1 al 100: ");
        int i=1;
        while(i<=100) {
            System.out.println(i);
            i=i+2;
        }

        i=2;
        System.out.println("Numeros pares 1 al 100: ");
        while(i<=100) {
            System.out.println(i);
            i=i+2;
        }
    }

}
